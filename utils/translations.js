// 이름 정리 함수들
export function cleanChampionName(name) {
  return name
    .replace("TFT15_", "")
    .replace("TFT_", "")
    .replace("TFT13_", "");
}

export function cleanTraitName(name) {
  return name
    .replace("TFT15_", "")
    .replace("Set15_", "")
    .replace("TFT_Trait_", "");
}

export function cleanItemName(name) {
  return name
    .replace("TFT_Item_", "")
    .replace("TFT15_", "");
}

export function cleanAugmentName(name) {
  return name
    .replace("TFT15_Augment_", "")
    .replace("TFT_Augment_", "");
}

// 한글 변환 함수들
export function toKoreanChampion(name) {
  const cleaned = cleanChampionName(name);
  return CHAMPION_KR[cleaned] || cleaned;
}

export function toKoreanTrait(name) {
  const cleaned = cleanTraitName(name);
  return TRAIT_KR[cleaned] || cleaned;
}

export function toKoreanItem(name) {
  const cleaned = cleanItemName(name);
  return ITEM_KR[cleaned] || cleaned;
}

export function toKoreanAugment(name) {
  const cleaned = cleanAugmentName(name);
  return AUGMENT_KR[cleaned] || cleaned;
}

// Data Dragon API URL 생성
export function getChampionImageUrl(championName) {
  const cleaned = cleanChampionName(championName);
  // TFT Set 15 버전 사용
  return `https://cdn.communitydragon.org/latest/tft-champion/${cleaned.toLowerCase()}.png`;
}

export function getItemImageUrl(itemName) {
  const cleaned = cleanItemName(itemName);
  return `https://cdn.communitydragon.org/latest/tft-item/${cleaned.toLowerCase()}.png`;
}

export function getTraitImageUrl(traitName) {
  const cleaned = cleanTraitName(traitName);
  return `https://cdn.communitydragon.org/latest/tft-trait/${cleaned.toLowerCase()}.png`;
}
